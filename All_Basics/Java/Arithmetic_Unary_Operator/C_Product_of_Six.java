package Java.Arithmetic_Unary_Operator;

import java.util.Scanner;

public class C_Product_of_Six {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n1 = sc.nextInt();
        int n2 = sc.nextInt();
        int n3 = sc.nextInt();
        int n4 = sc.nextInt();
        int n5 = sc.nextInt();
        int n6 = sc.nextInt();
        System.out.println(n1*n2*n3*n4*n5*n6);
    }
}
