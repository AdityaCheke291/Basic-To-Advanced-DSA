package Basic_To_Advanced_DataStructure.JavaBasicsToAdvanced_with_Anuj.Day_1;

public class B_DifferentOperators {
    public static void main(String[] args) {
        int a = 120, b = 30;
        // +, -, *, /, %
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
